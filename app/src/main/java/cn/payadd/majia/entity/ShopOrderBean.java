public class ShopOrderBean extends BaseBean {

    private DataBean data;

    public DataBean getData() {
        return data;
    }

    public void setData(DataBean data) {
        this.data = data;
    }

    public class DataBean {
        private int totalPage;
        private int page;
        private int row;
        private int dataCount;
        private List<DataListBean> dataList = new ArrayList<>();

        public int getTotalPage() {
            return totalPage;
        }

        public void setTotalPage(int totalPage) {
            this.totalPage = totalPage;
        }

        public int getPage() {
            return page;
        }

        public void setPage(int page) {
            this.page = page;
        }

        public int getRow() {
            return row;
        }

        public void setRow(int row) {
            this.row = row;
        }

        public int getDataCount() {
            return dataCount;
        }

        public void setDataCount(int dataCount) {
            this.dataCount = dataCount;
        }

        public List<DataListBean> getDataList() {
            return dataList;
        }

        public void setDataList(List<DataListBean> dataList) {
            this.dataList = dataList;
        }

        public class DataListBean {
            private String installmentOrderNo;
            private String refNo;
            private String orderNo;
            private String payOrderNo;
            private int orderStatus;
            private String orderAmt;
            private String orderTime;
            private String goodsPrice;
            private String consigneePhone;
            private int goodsQuantity;
            private String goodsPic;
            private String goodsName;
            private String orderDesc;
            private String goodsFreight;

            public String getInstallmentOrderNo() {
                return installmentOrderNo;
            }

            public void setInstallmentOrderNo(String installmentOrderNo) {
                this.installmentOrderNo = installmentOrderNo;
            }

            public String getRefNo() {
                return refNo;
            }

            public void setRefNo(String refNo) {
                this.refNo = refNo;
            }

            public String getOrderNo() {
                return orderNo;
            }

            public void setOrderNo(String orderNo) {
                this.orderNo = orderNo;
            }

            public String getPayOrderNo() {
                return payOrderNo;
            }

            public void setPayOrderNo(String payOrderNo) {
                this.payOrderNo = payOrderNo;
            }

            public int getOrderStatus() {
                return orderStatus;
            }

            public void setOrderStatus(int orderStatus) {
                this.orderStatus = orderStatus;
            }

            public String getOrderAmt() {
                return orderAmt;
            }

            public void setOrderAmt(String orderAmt) {
                this.orderAmt = orderAmt;
            }

            public String getOrderTime() {
                return orderTime;
            }

            public void setOrderTime(String orderTime) {
                this.orderTime = orderTime;
            }

            public String getGoodsPrice() {
                return goodsPrice;
            }

            public void setGoodsPrice(String goodsPrice) {
                this.goodsPrice = goodsPrice;
            }

            public String getConsigneePhone() {
                return consigneePhone;
            }

            public void setConsigneePhone(String consigneePhone) {
                this.consigneePhone = consigneePhone;
            }

            public int getGoodsQuantity() {
                return goodsQuantity;
            }

            public void setGoodsQuantity(int goodsQuantity) {
                this.goodsQuantity = goodsQuantity;
            }

            public String getGoodsPic() {
                return goodsPic;
            }

            public void setGoodsPic(String goodsPic) {
                this.goodsPic = goodsPic;
            }

            public String getGoodsName() {
                return goodsName;
            }

            public void setGoodsName(String goodsName) {
                this.goodsName = goodsName;
            }

            public String getOrderDesc() {
                return orderDesc;
            }

            public void setOrderDesc(String orderDesc) {
                this.orderDesc = orderDesc;
            }

            public String getGoodsFreight() {
                return goodsFreight;
            }

            public void setGoodsFreight(String goodsFreight) {
                this.goodsFreight = goodsFreight;
            }
        }
    }

}

